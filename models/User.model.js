const mongoose=require('mongoose');

const UserSchema=mongoose.Schema({
	fullName:{
		type:String
	},
	password:{
		type:String,
		required:true
	},
	email:{
		type:String,
		required:true
	},
	username:{
		type:String,
		required:true,
		unique:true
	},
	phone:{
		type:Number,
		required:true
	},
	gender:{
		type:String,
	},
	birthday:{
		type:String,
    },
    country:{
		type:String,
    },
    city:{
		type:String,
    },
    quarter:{
		type:String,
    },
    job:{
		type:String,
    },
    dateCreated:{
		type:Date,
    },
    dateUpdated:{
		type:Date,
    },
    followers:[String],
    followings:[String],
    profileUrl:{
		type:String,
    },
    role:{
		type:String,
    },
    history:[{
		collectionName:String,
		column_name:String,
		oldValue:String,
		newValue:String,
		updateAt: Date
    }]
})
module.exports=mongoose.model('User',UserSchema);